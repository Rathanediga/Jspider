class widngmain{
	public static void main(String[] args) {
		// widning
		int a=5;//32 bit
		System.out.println(a);
		double b=a;//64 bit
		System.out.println("widing : "+b);

		// narrowing
		double h=65.62;
		System.out.println(h);
		int l=(int)h;//casting using datatype
		System.out.println("narrowing : "+l);

	}
}